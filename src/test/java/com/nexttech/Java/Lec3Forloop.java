package com.nexttech.Java;

public class Lec3Forloop {

	public static void main(String[] args) {
		// for loop
		for(int i=0;i<10;i++) {
			System.out.println(i);
		}

for (int j=2;j<=100;j++) {
	System.out.println(j);
}
for (int k=0;k<=25;k=k+3) {
	System.out.println(k);
}
//for print all the odd number up to 25
for (int l=1;l<=25;l=l+2) {
	System.out.println(l);
}
// for print odd value from 0 to 25
for(int m=0;m<=25;m++){
System.out.println(m);
}

// for print even value from 0 to 24
for(int n=0;n<=24;n=n+2 ) {
System.out.println(n);	
}
	}

}
